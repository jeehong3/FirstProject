package com.farmstory.vo;

public class DiaryBook {

	private int diaNo;
	private String diaName;
	private String regPotNo;
	private int plaNo;
	private String plaName;
	private String plaType;
	private String memId;
	
	
	public int getDiaNo() {
		return diaNo;
	}
	public void setDiaNo(int diaNo) {
		this.diaNo = diaNo;
	}
	public String getDiaName() {
		return diaName;
	}
	public void setDiaName(String diaName) {
		this.diaName = diaName;
	}
	public String getRegPotNo() {
		return regPotNo;
	}
	public void setRegPotNo(String regPotNo) {
		this.regPotNo = regPotNo;
	}
	public int getPlaNo() {
		return plaNo;
	}
	public void setPlaNo(int plaNo) {
		this.plaNo = plaNo;
	}
	public String getPlaName() {
		return plaName;
	}
	public void setPlaName(String plaName) {
		this.plaName = plaName;
	}
	public String getPlaType() {
		return plaType;
	}
	public void setPlaType(String plaType) {
		this.plaType = plaType;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	
}
